num1 = 10
num2 = 2

#Mayor que
resultado = num1 > num2
print(resultado)

#Menor que
resultado = num1 < num2
print(resultado)

#Mayor o igual que
resultado = num1 >= num2
print(resultado)

#Menor o igual que
resultado = num1 <= num2
print(resultado)

#Diferente
resultado = num1 != num2
print(resultado)

#Igual
resultado = num1 == num2
print(resultado)